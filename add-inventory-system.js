function calculateInventoryParams(totalValue, itemName) {
  // –õ–æ–≥–∏–∫–∞ —Ä–∞—Å—á–µ—Ç–∞ –Ω–∞ –æ—Å–Ω–æ–≤–µ —Ç–∏–ø–∞ —Ç–æ–≤–∞—Ä–∞
  if (itemName.includes('–∫–≥') || itemName.includes('–ª')) {
    // –ñ–∏–¥–∫–æ—Å—Ç–∏ –∏ –ø–æ—Ä–æ—à–∫–∏ - –æ–±—ã—á–Ω–æ –æ—Ç 1 –¥–æ 10 –µ–¥–∏–Ω–∏—Ü
    const quantity = Math.ceil(totalValue / 200) || 1; // –ø—Ä–∏–º–µ—Ä–Ω–æ 200 —Ä—É–± –∑–∞ –µ–¥–∏–Ω–∏—Ü—É
    const pricePerUnit = Math.round((totalValue / quantity) * 100) / 100;
    const unit = itemName.includes('–∫–≥') ? '–∫–≥' : '–ª';
    return { quantity, pricePerUnit, unit };
  } else if (itemName.includes('—à—Ç') || itemName.includes('—É–ø') || itemName.includes('—Ä—É–ª–æ–Ω')) {
    // –®—Ç—É—á–Ω—ã–µ —Ç–æ–≤–∞—Ä—ã - –±–æ–ª—å—à–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ
    const quantity = Math.ceil(totalValue / 50) || 1; // –ø—Ä–∏–º–µ—Ä–Ω–æ 50 —Ä—É–± –∑–∞ —à—Ç—É–∫—É
    const pricePerUnit = Math.round((totalValue / quantity) * 100) / 100;
    return { quantity, pricePerUnit, unit: '—à—Ç' };
  } else if (itemName.includes('–ø–∞—Ä')) {
    // –ü–µ—Ä—á–∞—Ç–∫–∏ –≤ –ø–∞—Ä–∞—Ö
    const quantity = Math.ceil(totalValue / 25) || 1; // –ø—Ä–∏–º–µ—Ä–Ω–æ 25 —Ä—É–± –∑–∞ –ø–∞—Ä—É
    const pricePerUnit = Math.round((totalValue / quantity) * 100) / 100;
    return { quantity, pricePerUnit, unit: '–ø–∞—Ä' };
  } else {
    // –ü–æ —É–º–æ–ª—á–∞–Ω–∏—é - —à—Ç—É–∫–∏
    const quantity = Math.ceil(totalValue / 100) || 1; // –ø—Ä–∏–º–µ—Ä–Ω–æ 100 —Ä—É–± –∑–∞ —à—Ç—É–∫—É
    const pricePerUnit = Math.round((totalValue / quantity) * 100) / 100;
    return { quantity, pricePerUnit, unit: '—à—Ç' };
  }
}

